void	*ft_memcpy(void *dst, const void *src, unsigned int n)
{
	char				*d;
	char				*s;
	unsigned int		i;

	d = (char *)dst;
	s = (char *)src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
